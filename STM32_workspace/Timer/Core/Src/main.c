void tim1_init(){
	//set 1 sec for timer

	//RCC --> 16MHz --> psc(16000) --> 1000Hz

	//ARR = 1000
	__HAL_RCC_TIM1_CLK_ENABLE();
	uint16_t* CR1 = (uint16_t*)(0x40010000);
	uint16_t* DIER = (uint16_t*)(0x4001000c);

	uint16_t* ARR = (uint16_t*)(0x4001002c);
	uint16_t* PSC = (uint16_t*)(0x40010028);
	*ARR = 1000;
	*PSC = 16000 - 1; // vi psc = 0 ung voi bo chia la 1

	*DIER  |= (1<<0);//enable interrupt
	*CR1 |= (1 << 0); //enable time

	uint32_t* ISER0 = (uint32_t*)(0xe000e100);
	*ISER0 |= (1<<25);
}
uint32_t tim1_cnt = 0;

void TIM1_UP_TIM10_IRQHandler(){
	uint16_t* SR = (uint16_t*)(0x40010010);
	while(((*SR>>0)&1) != 1);//wait UTF flag is set to 1
	*SR &= ~(1<<0);//Clean UTF flag
	tim1_cnt++;
}

